  public boolean isValid() {
    try {
      String t = getText();
      if (t == null || t.trim().isEmpty()) return false;
      java.time.format.DateTimeFormatter dtf = java.time.format.DateTimeFormatter.ofPattern("HH:mm:ss");
      java.time.LocalTime.parse(t, dtf);
      return true;
    } catch (Exception e) {
      return false;
    }
  }

